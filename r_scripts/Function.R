# Object "die" that contains the numbers one through six
die <- 1:6

# Use the "sample" function for random sampling with replacement
sample(die, size = 2, replace = TRUE)


# Function roll_dice()
roll_dice <- function() {
  roll_dice <- function() {
    # Return the sum of eyes of rolling pair of dice
    roll_dice <- function(eyes = 1:6) {
      dice <- sample(eyes, size = 2, replace = TRUE)
      sum(dice)
    }
  }
}